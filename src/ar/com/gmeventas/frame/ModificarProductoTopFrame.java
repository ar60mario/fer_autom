    private void guardar() {
        int rowR = combo.getSelectedIndex();
        if (rowR < 1) {
            JOptionPane.showMessageDialog(this, "SELECCIONE EL RUBRO DEL PRODUCTO");
            return;
        }
        producto.setRubro(rubros.get(rowR - 1));
        if (activoChk.isSelected()) {
            producto.setActivo(true);
        } else {
            producto.setActivo(false);
            producto.setOrden(-1);
        }
        int rowS = combo.getSelectedIndex();
        if (rowS < 1) {
            JOptionPane.showMessageDialog(this, "SELECCIONE EL RUBRO DEL PRODUCTO");
            return;
        }
        producto.setSubRubro(subRubros.get(rowS - 1));
        Integer c = 0;
        if (!cantidadTxt.getText().isEmpty()) {
            c = Integer.valueOf(cantidadTxt.getText());
        }
        producto.setCantidad(c);
        try {
            new ProductoTopService().updateProductoTop(producto);
            JOptionPane.showMessageDialog(this, "Guardado correctamente");
        } catch (Exception ex) {
            Logger.getLogger(ModificarProductoTopFrame.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "No se actualizo Producto");
        }

    }
