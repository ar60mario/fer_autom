    private void llenarCampos() {
        rubros = null;
        subRubros = null;
        try {
            rubros = new RubroService().getAllRubros();
        } catch (Exception ex) {
            Logger.getLogger(ModificarProductoTopFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            subRubros = new SubRubroService().getAllSubRubros();
        } catch (Exception ex) {
            Logger.getLogger(ModificarProductoTopFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        combo.removeAllItems();
        combo.addItem("");
        comboS.removeAllItems();
        comboS.addItem("");
        if (rubros != null && !rubros.isEmpty()) {
            for (Rubro r : rubros) {
                combo.addItem(r.getNombre());
            }
        }
        if (subRubros != null && !subRubros.isEmpty()) {
            for (SubRubro s : subRubros) {
                comboS.addItem(s.getDetalle());
            }
        }
        int cod = producto.getCodigo();
        int codR = 0;
        if (producto.getRubro() != null) {
            codR = producto.getRubro().getCodigo();
        }
        int codS = 0;
        if (producto.getSubRubro() != null) {
            codS = producto.getSubRubro().getCodigo();
        }
        codigoTxt.setText(String.valueOf(cod));
        Producto pr = null;
        Configuracion co = null;
        try {
            pr = new ProductoService().getProductoByCodigo(cod);
            co = new ConfiguracionService().getFacturas(1L);
        } catch (Exception ex) {
            Logger.getLogger(ModificarProductoTopFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        Float iva = 0.0F;
        if (co != null) {
            iva = co.getIva() / 100;
        }
        if (pr != null) {
//            Rubro ru = pr.getRubro();

            int p = 1;
            int y = 0;
//            System.out.println(ru.getCodigo());
            System.out.println(rubros);
//            System.exit(0);
            if (rubros != null && !rubros.isEmpty()) {
                for (Rubro r : rubros) {
                    if (r.getCodigo().equals(codR)) {
                        y = p;
                    }
                    p += 1;
                }
            }
            combo.setSelectedIndex(y);
            y = 0;
            p = 1;
            if (subRubros != null && !subRubros.isEmpty()) {
                for (SubRubro r : subRubros) {
                    if (r.getCodigo().equals(codS)) {
                        y = p;
                    }
                    p += 1;
                }
            }
            comboS.setSelectedIndex(y);
            detalleTxt.setText(pr.getDetalle());
            Double precio = pr.getPrecio() + pr.getImpuesto() + (pr.getPrecio() * iva);
            precioTxt.setText(df.format(precio));
            detalleTxt.setEditable(false);
            precioTxt.setEditable(false);
            if (producto.getActivo()) {
                activoChk.setSelected(true);
            } else {
                activoChk.setSelected(false);
            }
            if (producto.getCantidad() != null) {
                cantidadTxt.setText(producto.getCantidad().toString());
            } else {
                cantidadTxt.setText("5");
            }
        }
    }

