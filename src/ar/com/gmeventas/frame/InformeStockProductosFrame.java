    private void llenarTabla() {
        List<Producto> productos = null;
        try {
            productos = new ProductoService().getAllProductos(); // SON ACTIVOS
        } catch (Exception ex) {
            Logger.getLogger(InformeStockProductosFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        BigDecimal suma = new BigDecimal("0.00");
        BigDecimal sumaImp = new BigDecimal("0.00");
        if (productos != null && !productos.isEmpty()) {
            DefaultTableModel tbl = (DefaultTableModel) tabla.getModel();
            for (Producto pr : productos) {
//                Integer co = pt.getCodigo();
//                Producto pr = null;
//                try {
//                    pr = new ProductoService().getProductoByCodigo(co);
//                } catch (Exception ex) {
//                    Logger.getLogger(InformeStockProductosFrame.class.getName()).log(Level.SEVERE, null, ex);
//                }
                Object o[] = new Object[5];
                o[0] = pr.getCodigo();
                o[1] = pr.getDetalle();
                if (pr.getStock() != null) {
                    o[2] = df1.format(pr.getStock());
                } else {
                    o[2] = df1.format(0);
                }
                o[3] = df.format(pr.getImpuesto());
                BigDecimal bd_can = new BigDecimal(df1.format(pr.getStock()).replace(",", "."));
                BigDecimal bd_neto = new BigDecimal(df.format(pr.getPrecio()).replace(",", "."));
                BigDecimal bd_impu = new BigDecimal(df.format(pr.getImpuesto()).replace(",", "."));
                BigDecimal bd_iva = new BigDecimal(df.format(1.21).replace(",", "."));
                BigDecimal bd_precio = bd_neto.multiply(bd_iva);
                bd_precio = bd_precio.add(bd_impu);
                BigDecimal bd_total = bd_can.multiply(bd_precio);
                sumaImp = sumaImp.add(bd_impu);
                Double ttl = bd_total.doubleValue();
                suma = suma.add(bd_total);
                o[4] = df.format(ttl);
                tbl.addRow(o);
            }
            Object o[] = new Object[5];
            o[1] = "SUMA TOTAL  >>>";
            o[3] = df.format(sumaImp.doubleValue());
            o[4] = df.format(suma.doubleValue());
            tbl.addRow(o);
            tabla.setModel(tbl);
        }
    }

